#include <bits/stdc++.h>
using namespace std;
int n, p, f[2][5000];
int main(){
	scanf("%d%d", &n, &p);
	f[1][1] = 1;
	for(int i = 2, k=0; i <= n; i++,k^=1)
		for(int j = 1; j <= i; j++)
			f[k][j] = (f[k][j-1]+f[!k][i-j])%p;
	printf("%d\n", (f[n&1][n]<<1)%p);
}
